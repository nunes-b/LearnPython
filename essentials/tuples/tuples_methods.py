# Index | -> ().index
linguagens = ("Java", "Python", "C",)
print(linguagens.index("Java"))
print(linguagens.index("Python"))
print("#--------------------------------------------------------------------#")

# Len | -> ().len
cars = ("BMW", "FORD", "HYUNDAI")
